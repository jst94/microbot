package net.runelite.client.plugins.microbot.util.models;

/**
 * Enum representing different types of interactions in the RuneLite client
 */
public enum InteractionType {
    /**
     * Interaction with a widget/UI element
     */
    WIDGET,

    /**
     * Interaction with an NPC
     */
    NPC,

    /**
     * Interaction with an object
     */
    OBJECT,

    /**
     * Interaction with an item
     */
    ITEM,

    /**
     * Generic interaction type
     */
    GENERIC
}
